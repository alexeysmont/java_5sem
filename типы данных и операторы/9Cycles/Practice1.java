public class Practice1 {
    public static void main(String[] args) {
        // бесконечный цикл while
        while (true) {
            System.out.println("Бесконечный while");
            break; 
        }
        
        // бесконечный цикл do-while
        do {
            System.out.println("Бесконечный do-while");
            break; 
        } while (true);
    }
}